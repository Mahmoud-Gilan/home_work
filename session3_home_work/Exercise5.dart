void main() {
  int a = 10;
  int b = 5;

  print("is a == b? ${a == b}"); // Check if a is equal to b
  print("is a != b? ${a != b}"); // Check if a is not equal to b
  print("is a > b? ${a > b}"); // Check if a is greater than b
  print("is a < b? ${a < b}"); // Check if a is less than b
  print("is a >= b? ${a >= b}"); // Check if a is greater than or equal to b
  print("is a <= b? ${a <= b}"); // Check if a is less than or equal to b

  int sum = a + b; // Add a and b
  print("sum == 20? ${sum == 20}"); // Check if the sum is equal to 20
}
