void main() {
  dynamic test = 10;
  print(test); // Output the value of test

  test = 'Bom Dia'; // Change the type of test to String
  print(test); // Output the new value of test

  var greeting = 'Hi';

  greeting = 'Bom dia'; // Change the value of greeting
  print(greeting);

  num pi = 3.14159;
  print(pi.toInt());

  print(pi.toStringAsFixed(3)); // Convert pi to a string with 3 decimal places
}
